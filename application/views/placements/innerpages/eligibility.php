<h3>Training and Placement Eligibility Criteria</h3>
<div <?php echo $class_;?>>
<p>
	All the students expected to graduate from AGI shall be eligible to participate in the CTPD activities. The participation is subjected to their registration with the CTPD.
</p>
<p>
	The various CTPD activities were displayed on Notice Boards/ e-Notice Boards and also be forwarded to the students via their ERP login, SMS and emails.
</p>
<p>
	The students who have minimum attendance of 60% in both theory and practical in every semester are eligible to sit in the campus interviews. Students below the 60% attendance need to take necessary approvals from Director of the Institute but any student below 50% attendance will not be eligible for registration with CTPD in any case. Similarly, attendance in PDP & Employability Training programmes organized by Training & Placement Department should also be 75%. Any failure towards the same will require approval from AGI Management Board to be able to participate in CTPD activities.
</p>
<p>
	All Directors and Principals are required to provide the semester wise attendance to the CTPD after every semester.
</p>