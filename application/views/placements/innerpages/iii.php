<p>
	<h5>Brief Insight to few activities at CTPD</h5>
	<b>In campus Training</b><br>
	AGI has signed MOU’s with various companies to have an expert pool ready for training students during their academic schedules. These trainings not only empower them with variety of technical skill sets but boost their confidence level too.
	<br><br>

	<b>Pre Placement Talks</b><br>
	HR managers form diverse spectrum are invited to the campus to have talks and discussion sessions with students, this helps them to have insight of what is expected from them at the time of campus placement drives.
	<br><br>

	<b>Expert Sessions and Visiting Lectures</b><br>
	CTPD organizes series of expert sessions and various experts from Industry and senior professors are invited to the campus as visiting lecturers during the 3rd and 4th year of their study.  Such series familiarize the students and the faculty with the new skill sets and latest domain specific knowledge that it beyond the routine academic activities.
