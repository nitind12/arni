    <b>Campus:</b> Buses, three-wheelers (autos) and cabs are easily available in Haldwani city for reaching the institute.<br>

    <b>Climate:</b> Nestled in the foothills of Nainital and situated in a serene location, away from the crowd the institute offers a lush green and safe and secured environment to its students. Generally, the climate of Haldwani is moderate. The winters are cold yet manageable. Summers are moderately warm. Rainy season is quite wet.
