<h5>Location</h5>
<p>
    <b>Destination Haldwani:</b> Nestled in Haldwani, the foothills of the famous tourist destination Nainital, Amrapali Group of Institutes is well connected by rail and road network. Almost adjacent to famous Jim Corbett Tiger Reserve the institute can be reached easily from Delhi, Dehradun and Lucknow both by road and rail network.<br><br>

